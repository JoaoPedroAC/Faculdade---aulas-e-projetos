// Exemplo de operadores de atribuição
// Atribuição basica de valores
let x = 10;
console.log(`Valor inicial de x: ${x}`); // 10

// Atribuição de adição
x += 5; // é equivalente a (x = x + 5)
console.log(`Após de x += 5 : ${x}`); // 15

// Atribuição de subtração
x -= 4; // é equivalente a (x = x - 5)
console.log(`Após de x -= 4: ${x}`); // 11

// Atribuição de multiplicação
x *= 7; // é equivalente a (x = x * 3)
console.log(`Após de x *= 7: ${x}`); // 77

// Atribuição de divisão
x /= 7; // é equivalente a (x = x / 3)
console.log(`Após de x /= 7: ${x}`); // 11

// Atribuição de resto ou módulo
x %= 2; // é equivalente a (x = x % 2)
console.log(`Após de x %= 2: ${x}`); // 1

// Atribuição de exponenciação
x **= 2; // é equivalente a (x = x ** 2)
console.log(`Após de x **= 2: ${x}`); // 1